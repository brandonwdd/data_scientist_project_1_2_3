def generate_lift_curve(y_true: pd.Series, y_pred_proba: np.ndarray, output_dir: str):
    """Generate lift curve"""
    # Sort by predicted probability
    sorted_indices = np.argsort(y_pred_proba)[::-1]
    sorted_y_true = y_true.iloc[sorted_indices].values
    
    # Cumulative lift
    n = len(y_true)
    cumulative_lift = np.cumsum(sorted_y_true) / (np.arange(1, n + 1) * y_true.mean())
    
    plt.figure(figsize=(10, 6))
    plt.plot(np.arange(1, n + 1) / n * 100, cumulative_lift)
    plt.xlabel('% of Population')
    plt.ylabel('Cumulative Lift')
    plt.title('Lift Curve')
    plt.grid(True)
    plt.savefig(os.path.join(output_dir, "lift_curve_topk.png"))
    plt.close()


def generate_segment_stability(
    X_test: pd.DataFrame,
    y_test: pd.Series,
    y_pred_proba: np.ndarray,
    output_dir: str
):
    """Generate segment stability report"""
    # Group by plan_tier if available
    if "plan_tier" in X_test.columns:
        segments = X_test["plan_tier"].unique()
        stability_data = []
        
        for segment in segments:
            mask = X_test["plan_tier"] == segment
            segment_metrics = compute_churn_metrics(
                y_test[mask], y_pred_proba[mask]
            )
            stability_data.append({
                "segment": segment,
                **segment_metrics
            })
        
        stability_df = pd.DataFrame(stability_data)
        stability_df.to_csv(
            os.path.join(output_dir, "segment_stability.csv"),
            index=False
        )


